#include "libft.h"

int		ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 1 || nb > 2147395600)
		return (0);
	while (i * i < nb)
		i++;
	return (i * i == nb ? i : 0);
}
